public class WallPaperDetail {

    private String id;
    private String name;
    private String author;
    private String kind_type;
    private String thumb_width;
    private String thumb_height;
    private String url;
    private String thumbUrl;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getKind_type() {
        return kind_type;
    }

    public void setKind_type(String kind_type) {
        this.kind_type = kind_type;
    }

    public String getThumb_width() {
        return thumb_width;
    }

    public void setThumb_width(String thumb_width) {
        this.thumb_width = thumb_width;
    }

    public String getThumb_height() {
        return thumb_height;
    }

    public void setThumb_height(String thumb_height) {
        this.thumb_height = thumb_height;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getThumbUrl() {
        return thumbUrl;
    }

    public void setThumbUrl(String thumbUrl) {
        this.thumbUrl = thumbUrl;
    }

}
